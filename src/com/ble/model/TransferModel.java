package com.ble.model;

public class TransferModel {

	public int num;
	public int touzhi;
	public int jiaoyi;
	public int type;
	public String zhongduan;
	public String date;
	public String time;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getTouzhi() {
		return touzhi;
	}

	public void setTouzhi(int touzhi) {
		this.touzhi = touzhi;
	}

	public int getJiaoyi() {
		return jiaoyi;
	}

	public void setJiaoyi(int jiaoyi) {
		this.jiaoyi = jiaoyi;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getZhongduan() {
		return zhongduan;
	}

	public void setZhongduan(String zhongduan) {
		this.zhongduan = zhongduan;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

}
