package com.ble.model;

public class TransferModel {

	public int num;
	public int touzhi;
	public int jiaoyi;
	public int type;
	public String zhongduan;
	public int date;
	public int time;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getTouzhi() {
		return touzhi;
	}

	public void setTouzhi(int touzhi) {
		this.touzhi = touzhi;
	}

	public int getJiaoyi() {
		return jiaoyi;
	}

	public void setJiaoyi(int jiaoyi) {
		this.jiaoyi = jiaoyi;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getZhongduan() {
		return zhongduan;
	}

	public void setZhongduan(String zhongduan) {
		this.zhongduan = zhongduan;
	}

	public int getDate() {
		return date;
	}

	public void setDate(int date) {
		this.date = date;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

}
